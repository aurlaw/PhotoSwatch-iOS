#import "UIColor+PhotoSwatch.h"

@implementation UIColor (UIColor_PhotoSwatch)

+ (UIColor *)photoSwatchDarkColor {
	return [UIColor colorWithHexString:@"#2D7F7E"];
	
}
+ (UIColor *)photoSwatchPrimaryColor {
	return [UIColor colorWithHexString:@"#aae5de"];

}
+ (UIColor *)photoSwatchSecondaryColor {
	return [UIColor colorWithHexString:@"#2d897e"];
}
/*
 
 Primary: #aae5de RGB(170, 229, 222)
 Secondary: #2d897e

 */

@end
